class client():
    def __init__(self, host, port):
        self.host = host
        self.port = port

    def send():
        print(f"Sending data to {self.host}:{self.port}")
    def receive():
        print(f"Receiving data from {self.host}:{self.port}")   
    def connect(self):
        print(f"Connecting to {self.host}:{self.port}") 